class ColourGenerator:
    
    def __init__(self):
        self.colourCounter = 0
    
    def getColour(self):
        if self.colourCounter < len(COLOR_BANK):
            self.colourCounter += 1
            return COLOR_BANK[self.colourCounter-1]
        else:
            rRandom = randint(0, 255)
            gRandom = randint(0, 255)
            bRandom = randint(0, 255)
            return (r"#{:02x}{:02x}{:02x}".format(rRandom, gRandom, bRandom))
    
    def resetColours(self):
        self.colourCounter = 0